def get_perspective(index: int) -> tuple[str, str]:
    """获取视角差异化指令（按 index 循环选取）

    返回 (prompt_section, short_name)：
    - prompt_section: 可直接追加到 system prompt 的文本段
    - short_name: 用于 JSON 输出和显示的视角短名
    """
    short_name, text = PERSPECTIVE_VARIANTS[index % len(PERSPECTIVE_VARIANTS)]
    return f"\n## 你的分析视角\n\n{text}\n", short_name


def SPEECH_CONCISE_HINT(char_limit: int) -> str:
    """发言长度软限制提示，char_limit=0 不提示"""
    if char_limit <= 0:
        return ""
    return (
        f"\n## 发言长度建议\n\n"
        f"请在充分表达观点的前提下尽量简洁，建议控制在 {char_limit} 字以内。"
        f"这不是硬性限制——如果论证需要更多篇幅，可以超出，但请避免不必要的冗长。\n"
        f"核心原则：宁可一针见血，不要面面俱到。每次不需要太多论点。\n"
    )


