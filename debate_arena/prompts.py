def USER_ROUNDTABLE_FREE_CHALLENGE(agent_name: str, all_arguments: str, remaining_count: int = 2,
                                    single_target: bool = False) -> str:
    """圆桌自由质疑指令 — remaining_count 为还需质疑的人次
    single_target=True 时限制只能质疑一个对象"""
    if single_target:
        target_instruction = "请选择 1 个你有异议的观点进行质疑。"
        target_warning = (
            "⚠️ 重要：你只能选择 1 个质疑对象，必须明确写出对方名字（如「我质疑【分析师2】的观点」）。\n"
            "不可同时质疑多人。如果未指定对象或指定多人，你将被要求重写。\n\n"
        )
    else:
        target_instruction = f"请选择 {remaining_count} 个你有异议的观点进行讨论，说明你的理由。"
        target_warning = (
            f"⚠️ 重要：你必须在质疑中明确写出你质疑的对象名字（如「我质疑【分析师2】的观点」），"
            f"不可省略。如果未指定对象，你将被要求重写。\n\n"
        )
    return (
        f"以下是所有参与者的论述：\n\n{all_arguments}\n\n"
        f"{target_instruction}\n"
        f"根据实际情况决定态度：如果是根本分歧，可以强烈质疑；"
        f"如果只是细节不同，也可以补充讨论而非全盘否定。\n\n"
        f"{target_warning}"
        f"同时给出你自己的预测。"
    )


